public class OperacionesMatematicas {
    public static void main(String[] args) {
        double x = 2.1;
        double y = 3;
        System.out.println(Math.ceil(x));//Un entero hacia arriba
        System.out.println(Math.floor(x));//entero hacia abajo
        System.out.println(Math.pow(x,y));//elevar un numero
        System.out.println(Math.max(x,y));//escoge el numero mayor de dos numeros
        System.out.println(Math.min(x,y));//escoge el numero menor de dos numeros
        System.out.println(Math.sqrt(y));//raiz cuadrada
        System.out.println(Math.PI* Math.pow(x,2));//pi*r2 area del circulo
    }
}
