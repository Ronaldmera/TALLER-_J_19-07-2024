public class decrementoIncremento {
    public static void main(String[] args) {
        int vidas = 5;
        vidas = vidas - 1;
        System.out.println(vidas);
        //decremento
        vidas --;
        System.out.println(vidas);
        //incremento
        vidas ++;
        System.out.println(vidas);
    }
}
